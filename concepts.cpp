#include <iostream>

int main() {
    int year = 2024;
    std::cout << "The year is " << year << "\n";

    /*
    Chaining variables and multiple cout's together
    */

    std::cout << "Let's chain some variables and text together\n";   
    int age = 23;

    std::cout << "Hello, I am ";
    std::cout << age;
    std::cout << " years old\n";


    return 0;

}